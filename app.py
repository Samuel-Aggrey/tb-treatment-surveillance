"""## Calculate Epidemiological Indicators

### Subtask:
Compute key epidemiological indicators such as treatment success rate, mortality rate, and treatment failure rate for each country and year based on the preprocessed data.

**Reasoning**:
I will calculate the 'Treatment Success Rate (new_sp)', 'Mortality Rate (new_sp)', and 'Treatment Failure Rate (new_sp)' columns as per the instructions, ensuring to handle division by zero errors by setting the rate to 0 when the cohort is zero. Then, I will display the first few rows of the DataFrame including these new columns to verify the calculations.
"""

df['Treatment Success Rate (new_sp)'] = ((df['new_sp_cur'] + df['new_sp_cmplt']) / df['new_sp_coh'] * 100).fillna(0)
df.loc[df['new_sp_coh'] == 0, 'Treatment Success Rate (new_sp)'] = 0


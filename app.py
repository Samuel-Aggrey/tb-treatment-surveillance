"""## Design Early Warning Alert System

### Subtask:
Implement an automated alert mechanism that detects unusual increases in TB cases using statistical methods.

**Reasoning**:
I will calculate the 3-year rolling mean and standard deviation of 'new_sp_coh' for each country. These rolling statistics will be used to establish a baseline for detecting unusual increases in TB cases.
"""

df['rolling_mean_3yr'] = df.groupby('country')['new_sp_coh'].rolling(window=3, min_periods=1).mean().reset_index(level=0, drop=True)
df['rolling_std_3yr'] = df.groupby('country')['new_sp_coh'].rolling(window=3, min_periods=1).std().reset_index(level=0, drop=True)

print("First 5 rows with rolling mean and std:")
print(df[['country', 'year', 'new_sp_coh', 'rolling_mean_3yr', 'rolling_std_3yr']].head())

"""**Reasoning**:
I will define an 'alert' condition where 'new_sp_coh' exceeds the 'rolling_mean_3yr' by two times the 'rolling_std_3yr', create a new boolean column 'alert' based on this condition, and then display the rows where an alert is present.


"""

df['alert'] = (df['new_sp_coh'] > (df['rolling_mean_3yr'] + 2 * df['rolling_std_3yr']))

alert_countries = df[df['alert']][['country', 'year', 'new_sp_coh', 'rolling_mean_3yr', 'rolling_std_3yr', 'alert']]

print('Countries with an alert for unusual increase in TB cases:')
print(alert_countries.head())

